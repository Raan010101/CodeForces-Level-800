#include <iostream>
using namespace std ; 

int main () {
    int x =0 ;
    int i = 0;  
    int n ; 

    cin>>n; 
    while(n--)
    {
        cin>>i; 
        if (i= 'X++')
        {
            x++ ; 
        }    

        else if (i = '--X')
        {
            x-- ;
        }
    }
}